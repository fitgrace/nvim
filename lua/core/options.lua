-- vim.g:   全局设置
-- vim.o:   获取或设置全局选项
-- vim.bo:  获取或设置缓冲区范围的选项
-- vim.wo:  获取或设置窗口范围的选项
-- vim.opt: 同时设置全局、窗口和缓冲区设置
-- vim.env: 获取或设置环境变量

local g = vim.g
local o = vim.o
local bo = vim.bo
local wo = vim.wo
local opt = vim.opt

------------------------------------------------------------------------------
-- 文件与编码
------------------------------------------------------------------------------
g.encoding = 'UTF-8'  -- Vim 内部使用 UTF-8 编码
o.fileencoding = 'UTF-8'  -- 文件默认保存编码为 UTF-8


------------------------------------------------------------------------------
-- 备份与 Undo
------------------------------------------------------------------------------
o.backup = false  -- 禁用备份
o.writebackup = false  -- 禁用写入备份
opt.swapfile = false  -- 禁用交换文件
opt.undofile = true  -- 启用持久化 Undo


------------------------------------------------------------------------------
-- 文件自动保存与读取
------------------------------------------------------------------------------
o.autowrite = true  -- 切换 buffer 时自动保存
o.autoread = true  -- 外部修改文件后自动重新加载
bo.autoread = true


------------------------------------------------------------------------------
-- 系统剪切板
------------------------------------------------------------------------------
opt.clipboard = 'unnamedplus'  -- 使用系统剪贴板


------------------------------------------------------------------------------
-- 光标与滚动
------------------------------------------------------------------------------
-- o.scrolloff = 8  -- 光标上下保留 8 行可见区域
-- o.sidescrolloff = 8  -- 光标左右滚动时保留 8 列可见区域


------------------------------------------------------------------------------
-- 行号与指示列
------------------------------------------------------------------------------
wo.number = true  -- 显示绝对行号
wo.relativenumber = false  -- 不显示相对行号
wo.cursorline = true  -- 高亮当前行
wo.signcolumn = 'no'  -- 不显示左侧指示符符号列


------------------------------------------------------------------------------
-- 缩进与 Tab
------------------------------------------------------------------------------
o.tabstop = 2  -- Tab 显示为 2 个空格宽度
bo.tabstop = 2
o.softtabstop = 2  -- 插入模式下 Tab 的软宽度
o.shiftround = true  -- >> << 时对齐到 shiftwidth 的倍数
o.shiftwidth = 2  -- 自动缩进时每级缩进为 2 个空格
bo.shiftwidth = 2
o.expandtab = true  -- 将 Tab 转为空格
bo.expandtab = true
o.autoindent = true  -- 自动继承上一行缩进
bo.autoindent = true
o.smartindent = true  -- 根据语法自动缩进


------------------------------------------------------------------------------
-- 搜索
------------------------------------------------------------------------------
vim.o.ignorecase = true  -- 搜索时忽略大小写
vim.o.smartcase = true   -- 若有大写字母，则开启智能大小写搜索
vim.o.hlsearch = true    -- 高亮搜索结果
vim.o.incsearch = true   -- 边输入边搜索


------------------------------------------------------------------------------
-- 历史记录与命令行
------------------------------------------------------------------------------
o.cmdheight = 1               -- 命令行高度为 1
o.history = 1000              -- 历史记录数量
o.backspace = 'indent,eol,start'  -- 启用 backspace 键的智能删除功能，允许删除缩进、行尾和行首的字符
o.whichwrap = 'b,s,<,>,[,],~' -- 允许在行首尾使用 <Left>/<Right> 跨行移动


------------------------------------------------------------------------------
-- 10. 延迟与交互
------------------------------------------------------------------------------
o.updatetime = 300  -- 触发自动保存及 CursorHold 事件的时间
o.timeoutlen = 500  -- 等待映射连击的时间（毫秒）


------------------------------------------------------------------------------
-- 折叠 (Folding)
------------------------------------------------------------------------------
opt.foldenable = true        -- 默认开启文件折叠
opt.foldnestmax = 1          -- 最大折叠层数为 1
wo.foldmethod = 'expr'       -- 使用表达式进行折叠
wo.foldexpr = 'nvim_treesitter#foldexpr()' -- 使用 treesitter 进行折叠
wo.foldlevel = 1             -- 打开文件时折叠层级为 1


------------------------------------------------------------------------------
-- 窗口拆分与折行
------------------------------------------------------------------------------
o.splitbelow = true          -- 水平分割窗口时，新窗口显示在下方
o.splitright = true          -- 垂直分割窗口时，新窗口显示在右侧
wo.wrap = true               -- 自动折行
o.whichwrap = '<,>,[,]'      -- 光标在行首尾时允许向上一行或下一行移动


------------------------------------------------------------------------------
-- 缓冲区与鼠标支持
------------------------------------------------------------------------------
o.hidden = true              -- 允许切换未经保存的 buffer
o.mouse = 'a'                -- 启用鼠标支持


------------------------------------------------------------------------------
-- 15. 终端颜色与字符显示
------------------------------------------------------------------------------
o.termguicolors = true      -- 启用 24 位色
opt.termguicolors = true
o.list = false              -- 不显示不可见字符
o.listchars = 'space:·,tab:··' -- 定义空格和 Tab 的可见字符
o.wildmenu = true           -- 命令行补全时显示菜单


opt.guifont = { 'FiraMono Nerd font Mono', ':h10' }

